package com.yellow.model;

public class Categories {
	public String categoryId;
	public String categoryName;
	public int categoryDeleteStatus;
	public String getCategoryId() {
		return categoryId;
	}
	public void setCategoryId(String categoryId) {
		this.categoryId = categoryId;
	}
	public String getCategoryName() {
		return categoryName;
	}
	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}
	public int getCategoryDeleteStatus() {
		return categoryDeleteStatus;
	}
	public void setCategoryDeleteStatus(int categoryDeleteStatus) {
		this.categoryDeleteStatus = categoryDeleteStatus;
	}
	
}
