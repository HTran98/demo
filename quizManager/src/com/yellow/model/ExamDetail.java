package com.yellow.model;

public class ExamDetail {
	String examDetailId;
	String answerSeleted;
	String examId;
	int examDetailDeleteStatus;
	String questionId;
	public String getExamDetailId() {
		return examDetailId;
	}
	public void setExamDetailId(String examDetailId) {
		this.examDetailId = examDetailId;
	}
	public String getAnswerSeleted() {
		return answerSeleted;
	}
	public void setAnswerSeleted(String answerSeleted) {
		this.answerSeleted = answerSeleted;
	}
	public String getExamId() {
		return examId;
	}
	public void setExamId(String examId) {
		this.examId = examId;
	}
	public int getExamDetailDeleteStatus() {
		return examDetailDeleteStatus;
	}
	public void setExamDetailDeleteStatus(int examDetailDeleteStatus) {
		this.examDetailDeleteStatus = examDetailDeleteStatus;
	}
	public String getQuestionId() {
		return questionId;
	}
	public void setQuestionId(String questionId) {
		this.questionId = questionId;
	}
	
}
